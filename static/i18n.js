// ── Core i18n engine ──────────────────────────────────────────────────────────

function getLang() {
  return localStorage.getItem('fleetLang') || 'en';
}

function applyLang(lang) {
  localStorage.setItem('fleetLang', lang);
  const t = I18N[lang];

  // Text nodes
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (t[key] !== undefined) el.textContent = t[key];
  });

  // Placeholder attributes
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    if (t[key] !== undefined) el.placeholder = t[key];
  });

  // Update toggle buttons
  document.querySelectorAll('.lang-btn').forEach(btn => {
    const isEN = btn.getAttribute('data-lang') === 'en';
    btn.classList.toggle('active', btn.getAttribute('data-lang') === lang);
  });
}

function setLang(lang) {
  applyLang(lang);
}

// Inject the language toggle bar into every page
document.addEventListener('DOMContentLoaded', () => {
  // Insert toggle at the top of every container
  const toggleHTML = `
    <div class="lang-bar" id="langBar">
      <button class="lang-btn" data-lang="en" onclick="setLang('en')">EN</button>
      <button class="lang-btn" data-lang="bg" onclick="setLang('bg')">БГ</button>
    </div>`;

  const container = document.querySelector('.container, .container-resolved, .container-maintenance');
  if (container) {
    container.insertAdjacentHTML('afterbegin', toggleHTML);
  }

  applyLang(getLang());
});
